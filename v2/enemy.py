class enemy:
    def __init__(self,xpos,ypos,speed):
        self.xpos = xpos
        self.ypos = ypos
        self.speed = speed

    def moveTowards(target):
        self.setheading(self.towards(target))
        self.forward(speed)

    def getX():
        return xpos

    def getY():
        return ypos

    
